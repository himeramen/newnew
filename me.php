			<h1>История заповедника Басеги</h1>
			<p class="content_info_p_p">Заповедник «Басеги» создавался долго и трудно. Его созданию предшествовали многолетние экспедиционные исследования ученых самых разных направлений. Изучением флоры и фауны хребта Басеги в 1934-1944 годах занимались Игошина К.Н. Так же здесь побывали участники комплексной экспедиции АН СССР в числе которой были известные орнитологи Воронцов Е.М. и Уварова В.Я.</p>
